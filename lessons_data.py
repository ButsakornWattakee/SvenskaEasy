# ----------------------------------------------------
# DYNAMIC DETAILED VOCABULARY TABLES GENERATION
# ----------------------------------------------------

for lesson in LESSONS:
    l_id = lesson["id"]
    vocab_map = {}
    
    # Gather items from typing practice
    if "typing_practice" in lesson:
        for tp in lesson["typing_practice"]:
            sw = tp["swedish"]
            # Clean descriptive headers in Thai
            thai_cleaned = tp["thai"].replace(" (สะกดคำว่า ฉัน ในภาษาสวีเดน)", "")                                      .replace(" (สะกดคำว่า คุณ ในภาษาสวีเดน)", "")                                      .replace(" (สะกดตัวพิมพ์เล็กทั้งหมด)", "")                                      .replace(" (พหูพจน์)", "")                                      .replace(" (apelsin)", "")                                      .replace(" (hus)", "")                                      .replace(" (flicka)", "")                                      .replace(" (telefon)", "")                                      .replace(" (ภาษาสวีเดนสะกดอย่างไร?)", "")                                      .strip()
            vocab_map[sw] = {
                "swedish": sw,
                "thai": thai_cleaned,
                "explanation": tp["explanation"]
            }
            
    # Gather items from matching practice
    if "matching_practice" in lesson:
        for mp in lesson["matching_practice"]:
            sw = mp["swedish"]
            thai_clean = mp["thai"]
            if " (" in thai_clean:
                thai_clean = thai_clean.split(" (")[0]
            if sw not in vocab_map:
                vocab_map[sw] = {
                    "swedish": sw,
                    "thai": thai_clean,
                    "explanation": "คำศัพท์หลักประจำบทเรียนสำหรับการจับคู่ภาพและการสนทนา"
                }
            else:
                vocab_map[sw]["thai"] = thai_clean

    # Build Markdown table
    if vocab_map:
        md_table = "\n\n| คำศัพท์ภาษาสวีเดน (Swedish) | คำแปลภาษาไทย (Thai) | คำอธิบาย / การใช้งาน (Description) |\n"
        md_table += "| :--- | :--- | :--- |\n"
        for sw, item in sorted(vocab_map.items()):
            md_table += f"| **{item['swedish']}** | {item['thai']} | {item['explanation']} |\n"
            
        # Append this as a new section to the lesson content
        lesson["sections"].append({
            "subtitle": "ตารางคำศัพท์หลักประจำบทเรียน (Key Vocabulary List)",
            "content": md_table
        })
